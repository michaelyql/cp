void solve() {
    string x;
    cin >> x;
    vector<bool> ok(10);
    for (char c : x) {
        ok[c - '0'] = true;
    } 
    char c;
    for (int i = 0; i <= 9; i++) {
        if (ok[i]) {
            c = i + '0';
            break;
        }
    }
    string ans(1, c);
    cout << ans << endl;
}

